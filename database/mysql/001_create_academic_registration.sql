CREATE DATABASE IF NOT EXISTS AcademicRegistrationDb
    CHARACTER SET utf8mb4
    COLLATE utf8mb4_unicode_ci;

USE AcademicRegistrationDb;

DROP TABLE IF EXISTS StudentSubjects;
DROP TABLE IF EXISTS Students;
DROP TABLE IF EXISTS Subjects;
DROP TABLE IF EXISTS Professors;

CREATE TABLE Professors
(
    Id CHAR(36) NOT NULL,
    FullName VARCHAR(120) NOT NULL,
    CONSTRAINT PK_Professors PRIMARY KEY (Id)
);

CREATE TABLE Subjects
(
    Id CHAR(36) NOT NULL,
    Code VARCHAR(12) NOT NULL,
    Name VARCHAR(120) NOT NULL,
    Credits INT NOT NULL,
    ProfessorId CHAR(36) NOT NULL,
    CONSTRAINT PK_Subjects PRIMARY KEY (Id),
    CONSTRAINT UQ_Subjects_Code UNIQUE (Code),
    CONSTRAINT CK_Subjects_Credits CHECK (Credits = 3),
    CONSTRAINT FK_Subjects_Professors FOREIGN KEY (ProfessorId) REFERENCES Professors (Id)
);

CREATE TABLE Students
(
    Id CHAR(36) NOT NULL,
    Name VARCHAR(120) NOT NULL,
    Email VARCHAR(180) NOT NULL,
    DocumentNumber VARCHAR(20) NOT NULL,
    CreatedAtUtc DATETIME(6) NOT NULL,
    UpdatedAtUtc DATETIME(6) NULL,
    CONSTRAINT PK_Students PRIMARY KEY (Id),
    CONSTRAINT UQ_Students_Email UNIQUE (Email),
    CONSTRAINT UQ_Students_DocumentNumber UNIQUE (DocumentNumber)
);

CREATE TABLE StudentSubjects
(
    StudentId CHAR(36) NOT NULL,
    SubjectId CHAR(36) NOT NULL,
    EnrolledAtUtc DATETIME(6) NOT NULL,
    CONSTRAINT PK_StudentSubjects PRIMARY KEY (StudentId, SubjectId),
    CONSTRAINT FK_StudentSubjects_Students FOREIGN KEY (StudentId) REFERENCES Students (Id) ON DELETE CASCADE,
    CONSTRAINT FK_StudentSubjects_Subjects FOREIGN KEY (SubjectId) REFERENCES Subjects (Id)
);

