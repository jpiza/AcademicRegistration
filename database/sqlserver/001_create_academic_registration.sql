IF DB_ID(N'AcademicRegistrationDb') IS NULL
BEGIN
    CREATE DATABASE AcademicRegistrationDb;
END
GO

USE AcademicRegistrationDb;
GO

IF OBJECT_ID(N'dbo.StudentSubjects', N'U') IS NOT NULL DROP TABLE dbo.StudentSubjects;
IF OBJECT_ID(N'dbo.Students', N'U') IS NOT NULL DROP TABLE dbo.Students;
IF OBJECT_ID(N'dbo.Subjects', N'U') IS NOT NULL DROP TABLE dbo.Subjects;
IF OBJECT_ID(N'dbo.Professors', N'U') IS NOT NULL DROP TABLE dbo.Professors;
GO

CREATE TABLE dbo.Professors
(
    Id UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_Professors PRIMARY KEY,
    FullName NVARCHAR(120) NOT NULL
);

CREATE TABLE dbo.Subjects
(
    Id UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_Subjects PRIMARY KEY,
    Code NVARCHAR(12) NOT NULL,
    Name NVARCHAR(120) NOT NULL,
    Credits INT NOT NULL CONSTRAINT CK_Subjects_Credits CHECK (Credits = 3),
    ProfessorId UNIQUEIDENTIFIER NOT NULL,
    CONSTRAINT UQ_Subjects_Code UNIQUE (Code),
    CONSTRAINT FK_Subjects_Professors FOREIGN KEY (ProfessorId) REFERENCES dbo.Professors (Id)
);

CREATE TABLE dbo.Students
(
    Id UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_Students PRIMARY KEY,
    Name NVARCHAR(120) NOT NULL,
    Email NVARCHAR(180) NOT NULL,
    DocumentNumber NVARCHAR(20) NOT NULL,
    CreatedAtUtc DATETIME2 NOT NULL,
    UpdatedAtUtc DATETIME2 NULL,
    CONSTRAINT UQ_Students_Email UNIQUE (Email),
    CONSTRAINT UQ_Students_DocumentNumber UNIQUE (DocumentNumber)
);

CREATE TABLE dbo.StudentSubjects
(
    StudentId UNIQUEIDENTIFIER NOT NULL,
    SubjectId UNIQUEIDENTIFIER NOT NULL,
    EnrolledAtUtc DATETIME2 NOT NULL,
    CONSTRAINT PK_StudentSubjects PRIMARY KEY (StudentId, SubjectId),
    CONSTRAINT FK_StudentSubjects_Students FOREIGN KEY (StudentId) REFERENCES dbo.Students (Id) ON DELETE CASCADE,
    CONSTRAINT FK_StudentSubjects_Subjects FOREIGN KEY (SubjectId) REFERENCES dbo.Subjects (Id)
);
GO

